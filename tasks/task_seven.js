//using the switch statment
const dayOfWeek = "Monday";

switch (dayOfWeek) {
  case "Monday":
    console.log("Monday: Start of the week");
    break;
  case "Tuesday":
    console.log("Tuesday: second day of the week");
    break;
  case "Wednesday":
    console.log("Wednesday: Midweek ");
    break;
  case "Thursday":
    console.log("Thursday: fourth day of the week");
    break;
  case "Friday":
    console.log("Friday: last working day of the week!");
    break;
  case "Saturday":
    console.log("Saturday: Weekend and party");
    break;
  case "Sunday":
    console.log("Sunday: church day and worship");
    break;
  default:
    console.log("you are wrong");
}
